"""Словарь DIC"""
dic = {
    "end": "конец",
    "for": "для",
    "as": "в качестве",
    "assert": "утверждать",
    "break": "перерыв",
    "class": "класс",
    "or": "или",
}

while len(dic):# Подсчет словаря
    for key in dic.keys() and dic.values():# Ключ : значения
        print(key, "\n")
        x = input("Перевод: ")
        if x != dic.keys:
            pass
    exit()


